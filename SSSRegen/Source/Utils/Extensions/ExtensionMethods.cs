namespace SSSRegen.Source.Utils.Extensions
{
    public static class ExtensionMethods
    {
        public static int ToInt(this float fl)
        {
            return (int) fl;
        }

        public static float ToFloat(this double dbl)
        {
            return (float) dbl;
        }

        public static int ToInt(this double dbl)
        {
            return (int) dbl;
        }
    }
}